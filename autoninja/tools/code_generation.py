    def _assess_complexity(self, functional_reqs: List[str]) -> str:
        """Assess complexity of requirements."""
        complex_indicators = ["analyze", "machine learning", "ai", "complex", "advanced", "intelligent"]
        
        complexity_score = 0
        for req in functional_reqs:
            req_lower = req.lower()
            complexity_score += sum(1 for indicator in complex_indicators if indicator in req_lower)
            complexity_score += len(req.split()) // 10  # Length factor
        
        if complexity_score >= 5:
            return "high"
        elif complexity_score >= 2:
            return "medium"
        else:
            return "low"
    
    def _select_foundation_model(self, complexity: str) -> str:
        """Select appropriate foundation model based on complexity."""
        if complexity == "high":
            return "anthropic.claude-3-opus-20240229-v1:0"
        elif complexity == "medium":
            return "anthropic.claude-3-sonnet-20240229-v1:0"
        else:
            return "anthropic.claude-3-haiku-20240307-v1:0"
  
    def _generate_agent_instruction(self, requirements: Dict[str, Any]) -> str:
        """Generate agent instruction based on requirements."""
        agent_type = requirements.get("agent_type_detected", "custom")
        functional_reqs = requirements.get("functional_requirements", [])
        
        base_instruction = f"You are a {agent_type} AI agent created by AutoNinja. "
        
        if agent_type == "conversational":
            base_instruction += "You excel at natural conversations and helping users with their questions and tasks. "
        elif agent_type == "analytical":
            base_instruction += "You specialize in data analysis, insights generation, and analytical reasoning. "
        elif agent_type == "automation":
            base_instruction += "You focus on automating tasks and workflows efficiently. "
        else:
            base_instruction += "You are designed to handle custom tasks as specified. "
        
        # Add specific capabilities
        if functional_reqs:
            capabilities = ", ".join(functional_reqs[:3])  # Top 3 requirements
            base_instruction += f"Your key capabilities include: {capabilities}. "
        
        base_instruction += "Always be helpful, accurate, and follow best practices. "
        base_instruction += "If you're unsure about something, ask for clarification rather than making assumptions."
        
        return base_instruction
    
    def _get_code_templates(self, requirements: Dict[str, Any]) -> List[Dict[str, Any]]:
        """Get relevant code templates from knowledge base."""
        if not self.kb_client:
            return []
        
        try:
            agent_type = requirements.get("agent_type_detected", "custom")
            query = f"code templates for {agent_type} agent implementation"
            
            results = self.kb_client.search_knowledge_base(
                kb_type=KnowledgeBaseType.CODE_TEMPLATES,
                query=query,
                max_results=5
            )
            
            templates = []
            for result in results:
                templates.append({
                    "template_name": result.title,
                    "description": result.excerpt,
                    "relevance_score": result.relevance_score,
                    "content": result.content[:1000] + "..." if len(result.content) > 1000 else result.content
                })
            
            return templates
            
        except Exception:
            return []
  

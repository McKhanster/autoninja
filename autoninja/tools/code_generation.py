class ActionGroupGeneratorTool(BaseTool):
    """Tool for generating Bedrock Agent action groups and tool integrations."""
    
    name: str = "action_group_generator"
    description: str = """Generate action groups and tool integrations for Bedrock Agents.
    Creates OpenAPI specifications and Lambda function implementations."""
    

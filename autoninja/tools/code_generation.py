  def _get_code_templates(self, requirements: Dict[str, Any]) -> List[Dict[str, Any]]:
        """Get relevant code templates from knowledge base."""
        if not self.kb_client:
            return []
        
        try:
            agent_type = requirements.get("agent_type_detected", "custom")
            query = f"code templates for {agent_type} agent implementation"
            
            results = self.kb_client.search_knowledge_base(
                kb_type=KnowledgeBaseType.CODE_TEMPLATES,
                query=query,
                max_results=5
            )
            
            templates = []
            for result in results:
                templates.append({
                    "template_name": result.title,
                    "description": result.excerpt,
                    "relevance_score": result.relevance_score,
                    "content": result.content[:1000] + "..." if len(result.content) > 1000 else result.content
                })
            
            return templates
            
        except Exception:
            return []  
  

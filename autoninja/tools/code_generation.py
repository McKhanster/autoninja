class BedrockAgentConfigInput(BaseModel):
    """Input schema for Bedrock Agent configuration generation tool."""
    requirements: Dict[str, Any] = Field(description="Agent requirements and specifications")
    architecture: Dict[str, Any] = Field(description="Service architecture design")


class BedrockAgentConfigTool(BaseTool):
    """Tool for generating Bedrock Agent configurations."""
    
    name: str = "bedrock_agent_config_generator"
    description: str = """Generate complete Bedrock Agent configurations including
    agent definitions, action groups, and knowledge base integrations."""
    
    args_schema: Type[BaseModel] = BedrockAgentConfigInput
    kb_client: Optional[BedrockKnowledgeBaseClient] = Field(default=None, exclude=True)
    
    def __init__(self, knowledge_base_client: Optional[BedrockKnowledgeBaseClient] = None, **kwargs):
        super().__init__(kb_client=knowledge_base_client, **kwargs)
   
    def _run(self, requirements: Dict[str, Any], architecture: Dict[str, Any]) -> str:
        """Generate Bedrock Agent configuration."""
        try:
            # Generate agent configuration
            agent_config = self._generate_agent_config(requirements, architecture)
            
            # Generate action groups
            action_groups = self._generate_action_groups(requirements, architecture)
            
            # Generate knowledge base configuration
            kb_config = self._generate_knowledge_base_config(requirements)
            
            # Get code templates from knowledge base
            code_templates = []
            if self.kb_client:
                code_templates = self._get_code_templates(requirements)
            
            result = {
                "agent_configuration": agent_config,
                "action_groups": action_groups,
                "knowledge_base_configuration": kb_config,
                "code_templates": code_templates,
                "deployment_artifacts": self._generate_deployment_artifacts(agent_config, action_groups)
            }
            
            return json.dumps(result, indent=2)
            
        except Exception as e:
            return f"Error generating Bedrock Agent configuration: {str(e)}"
    
    def _generate_agent_config(self, requirements: Dict[str, Any], architecture: Dict[str, Any]) -> Dict[str, Any]:
        """Generate Bedrock Agent configuration."""
        agent_type = requirements.get("agent_type_detected", "custom")
        functional_reqs = requirements.get("functional_requirements", [])
        
        # Determine foundation model based on complexity
        complexity = self._assess_complexity(functional_reqs)
        foundation_model = self._select_foundation_model(complexity)
        
        agent_config = {
            "agent_name": f"AutoNinja-{agent_type.title()}-Agent",
            "description": f"AI agent for {agent_type} tasks generated by AutoNinja",
            "foundation_model": foundation_model,
            "instruction": self._generate_agent_instruction(requirements),
            "idle_session_ttl_in_seconds": 3600,
            "agent_resource_role_arn": "arn:aws:iam::ACCOUNT:role/AmazonBedrockExecutionRoleForAgents_SUFFIX",
            "customer_encryption_key_arn": None,
            "tags": {
                "CreatedBy": "AutoNinja",
                "AgentType": agent_type,
                "Environment": "production"
            }
        }
        
        return agent_config

    def _generate_action_groups(self, requirements: Dict[str, Any], architecture: Dict[str, Any]) -> List[Dict[str, Any]]:
        """Generate action groups for the Bedrock Agent."""
        action_groups = []
        functional_reqs = requirements.get("functional_requirements", [])
        
        # Core action group for basic functionality
        core_actions = self._generate_core_action_group(functional_reqs)
        action_groups.append(core_actions)
        
        # Data processing action group if needed
        if any("data" in req.lower() or "process" in req.lower() for req in functional_reqs):
            data_actions = self._generate_data_action_group(functional_reqs)
            action_groups.append(data_actions)
        
        # Integration action group if needed
        if any("integrate" in req.lower() or "api" in req.lower() for req in functional_reqs):
            integration_actions = self._generate_integration_action_group(functional_reqs)
            action_groups.append(integration_actions)
        
        return action_groups


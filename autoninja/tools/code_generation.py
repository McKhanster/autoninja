    def _assess_complexity(self, functional_reqs: List[str]) -> str:
        """Assess complexity of requirements."""
        complex_indicators = ["analyze", "machine learning", "ai", "complex", "advanced", "intelligent"]
        
        complexity_score = 0
        for req in functional_reqs:
            req_lower = req.lower()
            complexity_score += sum(1 for indicator in complex_indicators if indicator in req_lower)
            complexity_score += len(req.split()) // 10  # Length factor
        
        if complexity_score >= 5:
            return "high"
        elif complexity_score >= 2:
            return "medium"
        else:
            return "low"
    

    kb_client: Optional[BedrockKnowledgeBaseClient] = Field(default=None, exclude=True)
    
    def __init__(self, knowledge_base_client: Optional[BedrockKnowledgeBaseClient] = None, **kwargs):
        super().__init__(kb_client=knowledge_base_client, **kwargs) 
       
    def _run(self, functional_requirements: List[str], integration_specs: Optional[Dict[str, Any]] = None) -> str:
        """Generate action groups and tool integrations."""
        try:
            # Analyze requirements to determine action groups
            action_groups = self._analyze_and_generate_action_groups(functional_requirements)
            
            # Generate OpenAPI specifications
            api_specs = self._generate_api_specifications(action_groups)
            
            # Generate Lambda implementations
            lambda_implementations = self._generate_lambda_implementations(action_groups)
            
            # Generate integration code if specs provided
            integrations = []
            if integration_specs:
                integrations = self._generate_integrations(integration_specs)
            
            result = {
                "action_groups": action_groups,
                "api_specifications": api_specs,
                "lambda_implementations": lambda_implementations,
                "integrations": integrations,
                "deployment_guide": self._generate_deployment_guide(action_groups)
            }
            
            return json.dumps(result, indent=2)
            
        except Exception as e:
            return f"Error generating action groups: {str(e)}" 
   
    def _analyze_and_generate_action_groups(self, functional_requirements: List[str]) -> List[Dict[str, Any]]:
        """Analyze requirements and generate appropriate action groups."""
        action_groups = []
        
        # Categorize requirements
        categories = self._categorize_requirements(functional_requirements)
        
        for category, requirements in categories.items():
            if not requirements:
                continue
                
            action_group = {
                "name": f"{category.title()}Actions",
                "description": f"Action group for {category} functionality",
                "category": category,
                "actions": self._generate_actions_for_category(category, requirements)
            }
            action_groups.append(action_group)
        
        return action_groups
    
    def _categorize_requirements(self, requirements: List[str]) -> Dict[str, List[str]]:
        """Categorize functional requirements."""
        categories = {
            "data": [],
            "communication": [],
            "analysis": [],
            "automation": [],
            "integration": [],
            "general": []
        }
        
        for req in requirements:
            req_lower = req.lower()
            
            if any(word in req_lower for word in ["data", "store", "retrieve", "database", "save"]):
                categories["data"].append(req)
            elif any(word in req_lower for word in ["email", "message", "notify", "send", "communicate"]):
                categories["communication"].append(req)
            elif any(word in req_lower for word in ["analyze", "report", "calculate", "process", "compute"]):
                categories["analysis"].append(req)
            elif any(word in req_lower for word in ["automate", "schedule", "trigger", "workflow"]):
                categories["automation"].append(req)
            elif any(word in req_lower for word in ["integrate", "api", "connect", "external"]):
                categories["integration"].append(req)
            else:
                categories["general"].append(req)
        
        return categories    


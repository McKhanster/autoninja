    name: str = "deployment_template_generator"
    description: str = """Generate CloudFormation templates for complete agent deployment
    including all necessary AWS resources and configurations."""
    
    args_schema: Type[BaseModel] = DeploymentTemplateInput
    
    def _run(self, agent_config: Dict[str, Any], infrastructure_requirements: Optional[Dict[str, Any]] = None) -> str:
        """Generate deployment CloudFormation template."""
        try:
            # Generate complete CloudFormation template
            template = self._generate_complete_template(agent_config, infrastructure_requirements)
            
            # Generate parameter files
            parameter_files = self._generate_parameter_files(agent_config)
            
            # Generate deployment scripts
            deployment_scripts = self._generate_deployment_scripts(agent_config)
            
            result = {
                "cloudformation_template": template,
                "parameter_files": parameter_files,
                "deployment_scripts": deployment_scripts,
                "deployment_instructions": self._generate_deployment_instructions()
            }
            
            return json.dumps(result, indent=2)
            
        except Exception as e:
            return f"Error generating deployment template: {str(e)}"    

